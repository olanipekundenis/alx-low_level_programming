#include "lists.h"

/**
 * add_nodeint_end - adds node at the rare of a linked list
 * @head: this adds pointer to the first element of the list
 * @n: it adds data to insert in the new element created in the node
 *
 * Return: this returns pointer to the new node, or NULL if it fails
 */
listint_t *add_nodeint_end(listint_t **head, const int n)
{
	listint_t *new_node;
	listint_t *temp = *head;

	new_node = malloc(sizeof(listint_t));
	if (!new_node)
		return (NULL);

	new_node->n = n;
	new_node->next = NULL;

	if (*head == NULL)
	{
		*head = new_node;
		return (new_node);
	}

	while (temp->next)
		temp = temp->next;

	temp->next = new_node;

	return (new_node);
}
